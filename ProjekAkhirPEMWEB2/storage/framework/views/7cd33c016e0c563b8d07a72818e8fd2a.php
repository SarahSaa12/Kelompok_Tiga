        <p><b>Perangkat keras komputer</b> bagian dari sistem komputer sebagai perangkat yang dapat diraba, 
            dilihat secara fisik, dan bertindak untuk menjalankan instruksi dari perangkat lunak (software). Perangkat 
            keras komputer juga disebut dengan hardware. Hardware berperan secara menyeluruh terhadap kinerja suatu 
            sistem komputer. Berikut cara kerja, macam-macam perangkat keras komputer, dan fungsinya.</p>
        <p><h5><b>A. Cara Kerja Perangkat Keras Komputer (Hardware)</b></h5>
            Hardware secara fundamental mempunyai bagian non-fisik yang disebut firmware. Firmware berfungsi sebagai 
            penyedia instruksi dasar yang dapat dilakukan sebuah hardware. Firmware disimpan dalam chip memori khusus 
            pada suatu hardware yang dapat berupa ROM atau EEPROM. Firmware berupa sebuah perangkat lunak kecil 
            (software) yang biasanya ditulis dalam bahasa pemrograman rendah untuk menghasilkan instruksi dasar 
            yang dapat digunakan oleh hardware bersangkutan secara cepat. Instruksi dasar ini dapat dibedakan 
            menjadi 3 yaitu,<br><br>
            <ul>
                <li>
                    <b>Instruksi Dasar Input<br></b>
                    Instruksi dasar input adalah instruksi yang dapat memberikan informasi kepada perangkat lunak 
                    lainnya yaitu sistem operasi dengan perantara driver. Hal ini terjadi saat pengguna berinteraksi 
                    dengan hardware bersangkutan. Misalnya mengetik dengan keyboard, menggeser mouse, dan lain-lain.<br><br>
                </li>
                <li>
                    <b>Instruksi Dasar Proses<br></b>
                    Instruksi dasar proses adalah instruksi yang berperan sebagai penengah proses input atau output 
                    yang dilakukan suatu hardware (tidak keduanya). Instruksi dasar proses yang dimaksud adalah 
                    instruksi yang hanya terjadi dalam suatu hardware atau bukan dalam sistem komputer secara utuh.<br><br>
                </li>
                <li>
                    <b>Instruksi Dasar Output<br></b>
                    Instruksi dasar output adalah instruksi yang dipanggil oleh perangkat lunak lainnya yaitu sistem 
                    operasi dengan perantara driver. Hal ini terjadi saat ada perintah untuk menggunakan hardware 
                    tersebut dari pengguna. Misalnya memutar video dengan monitor, mencetak dokumen dengan printer, 
                    dan lain-lain<br><br>
                </li>
            </ul>   
        </p>
